function convert() {
    let string = ''
    let text = lotin.value;
    for (let i = 0; i < text.length; i++) {
        let harf = text[i] + text[i + 1];
        if (letter[harf]) {
            string += letter[harf]
            i++
        }else{
        string += letter[text[i]]
    }
}
    kirill.value = string
    lotin.value = ''
}
